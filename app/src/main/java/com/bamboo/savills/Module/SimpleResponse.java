package com.bamboo.savills.Module;

public class SimpleResponse {

    /**
     * code : 0
     * codeDesc : OK
     */

    private int code;
    private String codeDesc;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getCodeDesc() {
        return codeDesc;
    }

    public void setCodeDesc(String codeDesc) {
        this.codeDesc = codeDesc;
    }
}
